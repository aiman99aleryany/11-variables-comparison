// Modify the second term of the expression in order to get true as result.

// 10 < 9
// 45 == 34
// true === 1
// false === 0
// 1 == 0
// 4 <= 2
// 4 >= 8

10 < 11; // 10 < 9;
45 == 45; // 45 == 34;
true === true; // true === 1;
false === false; // false === 0;
1 == 1; // 1 == 0;
4 <= 4; // 4 <= 2;
4 >= 4; // 4 >= 8;
